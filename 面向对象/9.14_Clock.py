import math

class Point(object):
    def __init__(self,x,y):
        self.x=x
        self.y=y
    def cal(self,object_x,object_y):
        return math.sqrt((object_x-self.x)*(object_x-self.x)+(object_y-self.y)*(object_y-self.y))

def main():
    point=Point(3,5)
    print(point.cal(-1,2))

if __name__ == '__main__':
    main()
